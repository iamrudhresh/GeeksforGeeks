class Solution {
  public:
    int nthFibonacci(int n){
        // code here
        int a=1,b=0,c;
        int modulo=1000000007;
        while(n--){
            c=(a+b)%modulo;
            a=b;
            b=c;
        }
        return c;
    }
};

